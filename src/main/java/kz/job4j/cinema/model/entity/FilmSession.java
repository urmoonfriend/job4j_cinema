package kz.job4j.cinema.model.entity;

import java.time.LocalDateTime;
import java.util.Map;
import java.util.Objects;

public class FilmSession {

    public static final Map<String, String> COLUMN_MAPPING = Map.of(
            "id", "id",
            "film_id", "filmId",
            "start_time", "startTime",
            "end_time", "endTime",
            "price", "price"
    );
    private Long id;
    private Long filmId;
    private Long hallId;
    private LocalDateTime startTime;
    private LocalDateTime endTime;
    private Integer price;

    public FilmSession() {}

    public FilmSession(Long id, Long filmId, Long hallId, LocalDateTime startTime, LocalDateTime endTime, Integer price) {
        this.id = id;
        this.filmId = filmId;
        this.hallId = hallId;
        this.startTime = startTime;
        this.endTime = endTime;
        this.price = price;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getFilmId() {
        return filmId;
    }

    public void setFilmId(Long filmId) {
        this.filmId = filmId;
    }

    public Long getHallId() {
        return hallId;
    }

    public void setHallId(Long hallId) {
        this.hallId = hallId;
    }

    public LocalDateTime getStartTime() {
        return startTime;
    }

    public void setStartTime(LocalDateTime startTime) {
        this.startTime = startTime;
    }

    public LocalDateTime getEndTime() {
        return endTime;
    }

    public void setEndTime(LocalDateTime endTime) {
        this.endTime = endTime;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        FilmSession filmSession = (FilmSession) o;
        return id == filmSession.id;
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }
}
